"""Calculadora"""


def sumar(a: float, b: float) -> float:
    """
    Suma dos números.

    Parameters
    ----------
    a : float
        Primer número.
    b : float
        Segundo número.

    Returns
    -------
    float
        La suma de los dos números.
    """
    return a + b


def restar(a: float, b: float) -> float:
    """
    Resta dos números.

    Parameters
    ----------
    a : float
        Minuendo.
    b : float
        Sustraendo.

    Returns
    -------
    float
        La resta de los dos números.
    """
    return a - b
